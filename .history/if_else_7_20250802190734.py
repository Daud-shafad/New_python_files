# Write a program that takes a grade (A, B, C, D, F) and prints a message. Example: "A" → "Excellent", "F" → "Fail".
grades = ["A", "B", "C", "D", "F"]
if grades == "A":
    print("Excellent!")
elif grades == "B":
    print("Good!")
elif grades == "C":
    print("Average!")
elif grades == "D":
    print("Below Average!")
elif grades == "F":
    print("Fail!")

